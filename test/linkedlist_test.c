static void test_linkedlist_init(void);


//******************************************************************************
// Function definitions
//******************************************************************************
int main(void)
{
    test_linkedlist_init();
}


//******************************************************************************
// Internal functions
//******************************************************************************
static void test_linkedlist_init(void)
{
    int data;
    linkedlist_t list = empty_list;
    list = linkedlist_add(list, &data);

    assert(list != empty_list);

    list = linkedlist_destroy(list);

    assert(list == empty_list);
}
